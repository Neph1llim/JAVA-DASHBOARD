    public boolean isValidEntry() {
        try {
            String gradeText = jTextField5.getText();
            if (gradeText.startsWith("Error")) {
                return false;
            }
            Double.parseDouble(gradeText);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }


